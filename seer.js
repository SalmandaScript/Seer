//dialog function
$(function() {
            $( "#dialog-7" ).dialog({
               autoOpen: true, 
               resize: function( event, ui ) {
                  $( this ).dialog( "option", "title",
	         ui.size.height +- " x " +- ui.size.width );
               }
            });
            $( "#opener-6" ).click(function() {
               $( "#dialog-7" ).dialog( "open" );
            });
         });
//end dialog function    

